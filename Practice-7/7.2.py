def roman_to_int(s):
    # Словарь для хранения соответствия между римскими и арабскими числами
    roman_dict = {'I': 1, 'V': 5, 'X': 10, 'L': 50, 'C': 100, 'D': 500, 'M': 1000}

    # Инициализация переменной для хранения результата
    result = 0

    # Цикл по каждому символу в строке
    for i in range(len(s)):
        # Если текущий символ меньше следующего, вычитаем его
        if i > 0 and roman_dict[s[i]] > roman_dict[s[i - 1]]:
            result += roman_dict[s[i]] - 2 * roman_dict[s[i - 1]]
        # В противном случае просто добавляем его
        else:
            result += roman_dict[s[i]]

    # Возвращаем результат
    return result

# Пример использования функции
print(roman_to_int("LVI"))
